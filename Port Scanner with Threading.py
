import socket
import threading
from queue import Queue

# Configuration
target = "127.0.0.1"  # Localhost (your own computer)
queue = Queue()
open_ports = []

def port_scan(port):
    """Attempts to connect to a specific port."""
    try:
        # Create a socket object
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        sock.settimeout(0.5) # Don't wait forever
        
        # result will be 0 if the connection is successful
        result = sock.connect_ex((target, port))
        if result == 0:
            open_ports.append(port)
        sock.close()
    except:
        pass

def worker():
    """Pulls port numbers from the queue and scans them."""
    while not queue.empty():
        port = queue.get()
        port_scan(port)

def run_scanner(port_range):
    # Fill the queue with ports (e.g., 1 to 1024)
    for port in range(1, port_range + 1):
        queue.put(port)

    # Start multiple threads to speed up the process
    thread_list = []
    for _ in range(50): # 50 workers
        t = threading.Thread(target=worker)
        thread_list.append(t)
        t.start()

    for t in thread_list:
        t.join()

    print(f"\nScanning finished on {target}")
    print(f"Open ports: {sorted(open_ports)}")


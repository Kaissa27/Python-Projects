import psutil
import time
from plyer import notification

def monitor_with_popups(threshold=80):
    print("Monitoring system... Alerts will appear as desktop notifications.")
    
    while True:
        cpu_usage = psutil.cpu_percent(interval=1)
        
        if cpu_usage > threshold:
            # Trigger a desktop notification
            notification.notify(
                title="🔥 System Alert: High CPU!",
                message=f"CPU usage is at {cpu_usage}%. Consider closing some apps.",
                app_name="Python Health Monitor",
                timeout=5 # Stays on screen for 5 seconds
            )
            # Sleep longer after a notification so we don't spam the user
            time.sleep(30) 
        
        time.sleep(5)


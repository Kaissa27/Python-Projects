import random

def play_hangman():
    words = ['python', 'programming', 'software', 'keyboard', 'computer']
    secret_word = random.choice(words)
    guessed_letters = []
    lives = 6
    
    # Create the display: ['_', '_', '_', ...]
    display_word = ["_"] * len(secret_word)

    print("--- WELCOME TO HANGMAN ---")

    while lives > 0 and "_" in display_word:
        print(f"\nWord: {' '.join(display_word)}")
        print(f"Lives: {lives} | Guessed: {', '.join(guessed_letters)}")
        
        guess = input("Guess a letter: ").lower()

        # 1. Validation: Check if it's one single letter
        if len(guess) != 1 or not guess.isalpha():
            print("Please enter a single letter.")
            continue
        
        if guess in guessed_letters:
            print(f"You already guessed '{guess}'!")
            continue

        guessed_letters.append(guess)

        # 2. Logic: Check if the letter is in the word
        if guess in secret_word:
            print(f"Good job! '{guess}' is in the word.")
            # Update underscores with the actual letter
            for i in range(len(secret_word)):
                if secret_word[i] == guess:
                    display_word[i] = guess
        else:
            print(f"Sorry, '{guess}' is not there.")
            lives -= 1

    # 3. End Game Results
    if "_" not in display_word:
        print(f"\n🎉 Congratulations! You guessed the word: {secret_word}")
    else:
        print(f"\n💀 Game Over! The word was: {secret_word}")

